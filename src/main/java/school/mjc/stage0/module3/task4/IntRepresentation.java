public class IntRepresentation {
    public static void main(String[] args) {
        int cInt = 99;
        int hInt = 104;
        int aInt = 97;
        int rInt = 114;
        char cChar = (char)cInt;
        char hChar = (char)hInt;
        char aChar = (char)aInt;
        char rChar = (char)rInt;
        String output = "" + cChar + hChar + aChar + rChar;
        System.out.println(output);
    }
}
